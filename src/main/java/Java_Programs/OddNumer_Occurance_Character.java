package Java_Programs;
public class OddNumer_Occurance_Character {
    // Find the character occurring Odd number of times example <a, b, c, b, c, a, c} = c
    public static void main (String[] args)
    {
    Character ch[] = {'a','b','c','b','c','a','c'};
    int count = ch.length;
    for (int i=0;i<count;i++)
    {
       System.out.println(ch[i]);
    }

    }
}
